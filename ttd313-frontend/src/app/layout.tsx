import type { Metadata } from 'next';
import './globals.css';
import Navigation from '@/components/navigation';
import Footer from '@/components/footer';
import { getStrapiData } from '@/utils/strapi-url';
import { globalSettingsQuery } from '@/utils/pageQueries';
import { RichTextElement } from '@/components/RichText/RichText';

interface GlobalSettingsAttributes {
  logo?: {
    data?: {
      attributes?: {
        url: string;
        alternativeText?: string;
      };
    };
  };
  socialMediaHandles?: {
    platform: string;
    url: string;
  }[];
  generalUpdates?: RichTextElement[];
  adHocInfo?: RichTextElement[];
  deliveryArea?: RichTextElement[];
}

// Fetch global settings
async function fetchGlobalSettings() {
  const response = await getStrapiData<GlobalSettingsAttributes>(
    'api/global-setting',
    globalSettingsQuery
  );

  // Type assertion to ensure response is a single item
  const singleResponse = response as {
    data: { attributes: GlobalSettingsAttributes };
  };

  return singleResponse.data?.attributes || {};
}

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const globalSettings = await fetchGlobalSettings();

  const navigationLinks = [
    { name: 'Home', href: '/' },
    { name: 'Menu', href: '/menu' },
    { name: 'About', href: '/facts-and-questions' },
  ];

  return (
    <html lang="en">
      <body className="antialiased lg:max-w-7xl mx-auto">
        {/* Navigation Component */}
        <Navigation
          navigation={navigationLinks}
          logo={globalSettings.logo?.data?.attributes}
        />

        {children}

        {/* Footer Component */}
        <Footer
          logo={globalSettings.logo}
          navigation={navigationLinks}
          socials={globalSettings.socialMediaHandles?.map((item) => ({
            name: item.platform,
            url: item.url,
          }))}
        />
      </body>
    </html>
  );
}
